<script lang="ts">
  import { onMount } from "svelte";

  const menuList = [
    { id: "s1", text: "Section1" },
    { id: "s2", text: "Section2" },
    { id: "s3", text: "Section3" },
    { id: "s4", text: "Section4" },
  ];
  let current = "s1";
  let scrollY = 0;
  let isMounted = false;

  function getTopPos(element: HTMLElement) {
    const box = element.getBoundingClientRect();
    const docElem = document.documentElement;
    return box.top + window.scrollY - docElem.clientTop;
  }

  $: if (isMounted) {
    for (let i = menuList.length - 1; i >= 0; i--) {
      const section = document.getElementById(menuList[i].id);
      if (section) {
        const topPos = getTopPos(section);
        if (scrollY + 100 > topPos) {
          current = menuList[i].id;
          break;
        }
      }
    }
  }

  onMount(() => {
    isMounted = true;
  });
</script>

