<script lang="ts">
  import { tweened } from "svelte/motion";
  import { sineInOut } from "svelte/easing";

  let app: HTMLDivElement;
  const pos = tweened(0, {
    duration: 500,
    easing: sineInOut,
  });
  const navList = [
    { href: "#sec1", text: "Section1" },
    { href: "#sec2", text: "Section2" },
    { href: "#sec3", text: "Section3" },
    { href: "#sec4", text: "Section4" },
  ];
  const onClickNav = (href: string) => {
    const target = app.querySelector(href);
    if (!target) throw new Error(`Cannot find target = ${href}`);
    const box = target.getBoundingClientRect();
    pos.set(box.top + window.scrollY - target.clientTop - 70);
  };

  $: {
    if (app) {
      document.body.scrollTop = $pos;
      document.documentElement.scrollTop = $pos;
    }
  }
</script>

<div class="container" bind:this={app}>
  <div id="wrapper">
    <header>
